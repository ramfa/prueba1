    private void actualizarBusqueda() {
    ArrayList<Docente> result = null;
        if (String.valueOf(cboParametroCli.getSelectedItem()).equalsIgnoreCase("Codigo")) {
            result = BDDocente.listarDocentePorCodigo(txtBuscarCliente.getText());

        } else if (String.valueOf(cboParametroCli.getSelectedItem()).equalsIgnoreCase("Nombre")) {
            result = BDDocente.listarDocentePorNombre(txtBuscarCliente.getText());
        }
        recargarTable(result);
    }

    public void recargarTable(ArrayList<Docente> list) {
       try{ Object[][] datos = new Object[list.size()][5];
        int i = 0;
        for (Docente d : list) {
            datos[i][0] = d.getLegajo();
            datos[i][1] = d.getNombre();
            datos[i][2] = d.getApellido();
            datos[i][3] = d.getCargoId();
            datos[i][4] = d.getAsignaturaId();
            i++;
        }
        jtDocente.setModel(new javax.swing.table.DefaultTableModel(
                datos,
                new String[]{
                    "Legajo", "Nombre","Apellido","CargoId","Asignatura"
                }) {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        });
        }catch(Exception e){
            System.out.println(e);
        }
    }

