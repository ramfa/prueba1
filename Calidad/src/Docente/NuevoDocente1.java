    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
 if(evt.getActionCommand().equals("boton"))
            txtCodigo.enable(false);
            txtrSoc.enable(true);
            txtdir.enable(true);
            txtcp.enable(true);
            txtmail.enable(true);
            txttel.enable(true);
              txtCodigo.setText("");
            txtrSoc.setText("");
            txtdir.setText("");
            txtcp.setText("");
            txtmail.setText("");
            txttel.setText("");
         cont++;
        
         txtCodigo.setText(""+cont++);
         repaint();
         limpiar();
         Docente d  = new Docente();
        try {
            Connection cn = BD.getConnection();
            Statement stmt = cn.createStatement();
            ResultSet rs = stmt.executeQuery("select max(legajo) from docente");
            while (rs.next()) {
                txtCodigo.setText(String.valueOf(rs.getInt(1)+1));
            }
            rs.close();
            stmt.close();
            cn.close();
        } catch (Exception error) {
            System.out.println(error);
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
  if(txtCodigo.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Ingrese un Legajo para Buscar");
   }else{
        Docente d= new Docente();
        d.setLegajo(Integer.parseInt(txtCodigo.getText()));
        if(d.buscar()){
            txtrSoc.setText(d.getNombre());
            txtdir.setText(d.getApellido());
            txtcp.setText(""+d.getCargoId());
            txttel.setText(""+d.getAsignaturaId());
            txtrSoc.enable(false);
            txtdir.enable(false);
            txtmail.enable(false);
            txtcp.enable(false);
            txttel.enable(false);
        }else{
            JOptionPane.showMessageDialog(this, "No se encontraron coincidencias. Intentelo nuevamente");
             }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed
